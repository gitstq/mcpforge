#!/usr/bin/env python3
"""
Init Command - Initialize a new MCP server project
"""

import os
import json
from pathlib import Path
from argparse import Namespace

from mcpforge.utils.formatting import print_success, print_error, print_info
from mcpforge.utils.colors import Colors


# Template for Python MCP server
PYTHON_TEMPLATE = '''#!/usr/bin/env python3
"""
{name} - MCP Server
Generated by MCPForge
"""

from mcp.server import Server
from mcp.types import TextContent

# Initialize server
server = Server("{name}")


@server.tool()
def example_tool(message: str) -> str:
    """
    An example tool that processes a message.
    
    Args:
        message: The input message to process
        
    Returns:
        Processed message
    """
    return f"Processed: {{message}}"


@server.resource("greeting://{{name}}")
def greeting_resource(name: str) -> str:
    """
    A greeting resource.
    
    Args:
        name: Name to greet
        
    Returns:
        Greeting message
    """
    return f"Hello, {{name}}!"


@server.prompt()
def example_prompt() -> str:
    """
    An example prompt template.
    """
    return "This is an example prompt."


if __name__ == "__main__":
    server.run()
'''


PYTHON_REQUIREMENTS = '''mcp>=1.0.0
'''


