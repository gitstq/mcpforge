def execute(args: Namespace) -> int:
    """Execute the init command."""
    name = args.name
    template = args.template
    path = args.path
    
    if not name:
        name = input("Project name: ").strip()
        if not name:
            print_error("Project name is required")
            return 1
    
    # Create project directory
    project_path = Path(path) / name if path != "." else Path(name)
    
    if project_path.exists():
        print_error(f"Directory {project_path} already exists")
        return 1
    
    project_path.mkdir(parents=True)
    print_info(f"Creating new MCP server project: {name}")
    print_info(f"Template: {template}")
    print_info(f"Location: {project_path.absolute()}")
    
    # Generate project based on template
    if template == "python":
        return generate_python_project(project_path, name)
    elif template in ["typescript", "javascript"]:
        return generate_typescript_project(project_path, name)
    else:
        print_error(f"Template '{template}' not yet implemented")
        return 1


def generate_python_project(project_path: Path, name: str) -> int:
    """Generate a Python MCP server project."""
    # Create directory structure
    src_dir = project_path / name.replace("-", "_")
    src_dir.mkdir()
    
    # Create main server file
    server_file = src_dir / "server.py"
    with open(server_file, 'w') as f:
        f.write(PYTHON_TEMPLATE.format(name=name))
    
    # Create __init__.py
    init_file = src_dir / "__init__.py"
    with open(init_file, 'w') as f:
        f.write(f'"""{name} MCP Server"""\n')
    
    # Create requirements.txt
    req_file = project_path / "requirements.txt"
    with open(req_file, 'w') as f:
        f.write(PYTHON_REQUIREMENTS)
    
    # Create pyproject.toml
    pyproject_file = project_path / "pyproject.toml"
    with open(pyproject_file, 'w') as f:
        f.write(PYTHON_PYPROJECT.format(
            name=name,
            module_name=name.replace("-", "_")
        ))
    
    # Create README.md
    readme_file = project_path / "README.md"
    with open(readme_file, 'w') as f:
        f.write(generate_readme(name, "python"))
    
    # Create mcpforge.json
    mcpforge_file = project_path / "mcpforge.json"
    with open(mcpforge_file, 'w') as f:
        json.dump({
            "name": name,
            "version": "0.1.0",
            "type": "mcp-server",
            "template": "python",
            "entry": f"{name.replace('-', '_')}.server:main"
        }, f, indent=2)
    
    print_success(f"Created Python MCP server project: {name}")
    print()
    print("Next steps:")
    print(f"  cd {project_path.name}")
    print("  pip install -e .")
    print(f"  python -m {name.replace('-', '_')}.server")
    
    return 0


def generate_typescript_project(project_path: Path, name: str) -> int:
    """Generate a TypeScript MCP server project."""
    # Create directory structure
    src_dir = project_path / "src"
    src_dir.mkdir()
    
    # Create main server file
    server_file = src_dir / "index.ts"
    with open(server_file, 'w') as f:
        f.write(TYPESCRIPT_TEMPLATE.format(name=name))
    
    # Create package.json
    pkg_file = project_path / "package.json"
    with open(pkg_file, 'w') as f:
        f.write(TYPESCRIPT_PACKAGE_JSON.format(name=name))
    
    # Create tsconfig.json
    tsconfig_file = project_path / "tsconfig.json"
    with open(tsconfig_file, 'w') as f:
        f.write(TYPESCRIPT_CONFIG)
    
    # Create README.md
    readme_file = project_path / "README.md"
    with open(readme_file, 'w') as f:
        f.write(generate_readme(name, "typescript"))
    
    # Create mcpforge.json
    mcpforge_file = project_path / "mcpforge.json"
    with open(mcpforge_file, 'w') as f:
        json.dump({
            "name": name,
            "version": "0.1.0",
            "type": "mcp-server",
            "template": "typescript",
            "entry": "dist/index.js"
        }, f, indent=2)
    
    print_success(f"Created TypeScript MCP server project: {name}")
    print()
    print("Next steps:")
    print(f"  cd {project_path.name}")
    print("  npm install")
    print("  npm run build")
    print("  npm start")
    
    return 0


